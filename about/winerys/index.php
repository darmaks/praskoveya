                           <p>Виноградники  расположены  на холмах напротив моря и благодаря морским бризам, приносящим с собой свежесть и влагу, хорошо справляются с летним зноем. Средиземноморский климат и мягкость температур способствуют хорошему созреванию винограда. Воздух наполнен ароматами сосновой хвои, миндаля, лаванды, тимьяна, розмарина и ладанника. Уникальные почвенно-климатические условия вместе с технологиями разумного землепользования позволяют хозяйству производить великолепные терруарные вина, получающие высокие отметки ведущих винных гидов мира.</p>
