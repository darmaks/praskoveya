                           <p>В самом центре французского  региона Лангедок-Руссильон расположено поместье Сент-Мартин де ла Гарриг (Saint Martin de la Garrigue) - одно из самых красивых винодельческих хозяйств на юге Франции, владельцем которого с 2011 года является АО&nbsp;«Прасковейское».</p>
